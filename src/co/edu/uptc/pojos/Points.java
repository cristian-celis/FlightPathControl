package co.edu.uptc.pojos;

public class Points {
    private int coorX;
    private int coorY;

    public Points(int coorX, int coorY){
        this.coorX = coorX;
        this.coorY = coorY;
    }

    public int getCoorX() {
        return coorX;
    }

    public int getCoorY() {
        return coorY;
    }
}
